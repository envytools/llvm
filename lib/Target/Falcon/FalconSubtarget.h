#ifndef LLVM_LIB_TARGET_FALCON_FALCONSUBTARGET_H
#define LLVM_LIB_TARGET_FALCON_FALCONSUBTARGET_H

#include "FalconFrameLowering.h"
#include "FalconISelLowering.h"
#include "FalconInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"
#include "llvm/Target/TargetSubtargetInfo.h"

#define GET_SUBTARGETINFO_HEADER
#include "FalconGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class FalconSubtarget : public FalconGenSubtargetInfo {
  virtual void anchor();
  FalconInstrInfo InstrInfo;
  FalconFrameLowering FrameLowering;
  FalconTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

  int FalconVersion;
  bool IsFalcon034;
  bool IsFalcon3p;
  bool IsFalcon4p;
  bool IsFalcon5p;
  bool HasCrypt;

public:
  // This constructor initializes the data members to match that
  // of the specified triple.
  FalconSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
               const TargetMachine &TM);

  // ParseSubtargetFeatures - Parses features string setting specified
  // subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  const FalconInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const FalconFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const FalconTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
  const TargetRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }
};
} // End llvm namespace

#endif
